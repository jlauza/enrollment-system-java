    public ArrayList<Course_1> courseList() {
        ArrayList<Course_1> course = new ArrayList<Course_1>();
        //Connection connection = getConnection();
        String sql = "SELECT * FROM course";
        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery(sql);
            Course_1 c;
            while (rs.next()) {
            c = new Course_1(rs.getInt("course_dbid"),rs.getString("CourseName"),rs.getString("Abbrevation"),rs.getString("DegreeLevel"));
            course.add(c);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        return course;
    }
    
