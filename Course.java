        String sql = "INSERT INTO course "
                + "(CourseName,Abbrevation,DegreeLevel) "
                + "VALUES (?,?,?)";   
        try(PreparedStatement stmt = con.prepareStatement(sql)){

            String name = coursename.getText();
            String acro = courseacro.getText();
            String degree = combodegree.getSelectedItem().toString();

            stmt.setString(1, name);
            stmt.setString(2, acro);
            stmt.setString(3, degree);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "New course added!");
            update_table();
            coursename.setText("");
            courseacro.setText("");
            combodegree.setEnabled(false);
            coursename.setEnabled(false);
            courseacro.setEnabled(false);
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
