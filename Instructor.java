    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = "INSERT INTO instructors "
                + "(InstructorID,Firstname,Middlename,Lastname,Gender,Email) "
                + "VALUES (?,?,?,?,?,?)";   
        try(PreparedStatement stmt = con.prepareStatement(sql)){
            int sid = Integer.parseInt(insID.getText());
            String fname = insfname.getText();
            String mname = insmname.getText();
            String lname = inslname.getText();
            String gender = insgender.getSelectedItem().toString();
            String email = insemail.getText();
            stmt.setInt(1, sid);
            stmt.setString(2, fname);
            stmt.setString(3, mname);
            stmt.setString(4, lname);
            stmt.setString(5, gender);
            stmt.setString(6, email);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "New Instructor added!");
            update_table();
            
            insID.setText("");
            insfname.setText("");
            insmname.setText("");
            inslname.setText("");
            insemail.setText("");
            
            insID.setEditable(false);
            insfname.setEditable(false);
            insmname.setEditable(false);
            inslname.setEditable(false);
            insgender.setEnabled(false);
            insemail.setEditable(false);
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

