    public ArrayList<Instructors> instructorList() {
        ArrayList<Instructors> instructor = new ArrayList<Instructors>();
        //Connection connection = getConnection();
        String sql = "SELECT * FROM instructors";
        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery(sql);
            Instructors c;
            while (rs.next()) {
            c = new Instructors(rs.getInt("Instructor_dbid"),rs.getInt("InstructorID"),rs.getString("Firstname"),rs.getString("Middlename"),rs.getString("Lastname"),rs.getString("Gender"),rs.getString("Email"));
            instructor.add(c);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        return instructor;
    }
    
